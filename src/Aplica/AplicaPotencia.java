    private void btnCalcularActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCalcularActionPerformed
        // TODO add your handling code here:
        double x,y, pot;
        if (txtA.getText().isEmpty()) {
            JOptionPane.showMessageDialog(this,"Ingrese un Valor Numerico",
                                                "Ventana de Error",JOptionPane.ERROR_MESSAGE);
            
        } else {
            x=Double.parseDouble(txtA.getText().trim());
            y=Double.parseDouble(txtB.getText().trim());
            pot=Matematica.pow(x,y);
            
            txtResultado.setText("\t Resultado");
            txtResultado.append("\n \t --------------------");
            txtResultado.append("\n Numero Base : "+ String.valueOf(x));
            txtResultado.append("\n Numero Exponente : "+ String.valueOf(y));
            txtResultado.append("\n resultado : "+ String.valueOf(pot));
        }
    }//GEN-LAST:event_btnCalcularActionPerformed

