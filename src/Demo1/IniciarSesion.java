    private void btnIngresarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnIngresarActionPerformed
  
       String[] Usuarios={"Maria","Noemi"};
        String [] Claves=new String[2];
        Claves[0]="12345";//clave maria
        Claves[1]="123456789";
        
        String usuario=txtUsuario.getText();
        String password=clave.getText();
        boolean mensaje=false;
        for (int i = 0; i < Usuarios.length; i++) {
            if(Usuarios[i].equals(usuario)&&Claves[i].equals(password)){
                mensaje=true;
            }
        }
        if(mensaje){
            CAplica_Portada caso=new CAplica_Portada();
            caso.setVisible(true);
            this.dispose();
        }else{
            JOptionPane.showMessageDialog(null, "Intente otra vez");
             txtUsuario.setText("");
             clave.setText("");
             
        }


        this.setVisible(false );
         
    }//GEN-LAST:event_btnIngresarActionPerformed

