    String valida() {
        if (cboProducto.getSelectedIndex() == -1) {
            cboProducto.requestFocus();
            return "Descripcion del Producto";
        } else if (txtCantidad.getText().equals("")
                || Integer.parseInt(txtCantidad.getText()) < 0) {
            txtCantidad.setText("");
            txtCantidad.requestFocus();
            return "Cantidad comprada";
        } else {
            return "";
        }
    }

    void imprimir(double TipoCuaderno, double preciocuaderno, double cantidad, 
          double descuento, double neto, double subtotal) {
        int productos = cboProducto.getSelectedIndex();
        DefaultListModel moR = new DefaultListModel();
        moR.addElement("El tipo de cuaderno es: "+cboProducto.getSelectedItem());
        moR.addElement("El precio del cuaderno es: " + preciocuaderno);
        moR.addElement("La Cantida de cuadernos es: " + cantidad);
        moR.addElement("El sub total es: " + subtotal);
        moR.addElement("El descuento es: " + String.format("%.2f", descuento));
        moR.addElement("El Monto total a pagar es: " + neto);
        lisResultado.setModel(moR);
    }

    void limpiarControles() {
        cboProducto.setSelectedIndex(-1);
        txtCantidad.setText("");
        lisResultado.setVisible(false);
        cboProducto.requestFocus();
    }

