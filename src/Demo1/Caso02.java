    private void btnComprarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnComprarActionPerformed
        // TODO add your handling code here:

        if (valida().equals("")) {
            claseCuaderno per1 = new claseCuaderno();
            per1.setTipoCuaderno(cboProducto.getSelectedIndex());
            per1.setPreciocuaderno(cboProducto.getSelectedIndex());
            per1.setCantidad(Double.parseDouble(txtCantidad.getText()));
            

            double cantidad = per1.MostrarCantida();
            double cuadernos = per1.TiposdeCuadernos();
            double subtotal = per1.Subtotal();
            double descuento = per1.DescuentoCuadernos();
            double neto = per1.SueldoNeto();
            double preciocuaderno = per1.TiposdeCuadernos();
            

            imprimir(cuadernos, preciocuaderno, cantidad, descuento, neto, subtotal);

        } else {
            JOptionPane.showMessageDialog(null,
                    "El error esta en " + valida());
        }
        lisResultado.setVisible(true);
    }//GEN-LAST:event_btnComprarActionPerformed

